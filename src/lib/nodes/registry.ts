export const NODE_MAP: Record<string, NodeDefinition> = Object.fromEntries(
  NODE_DEFINITIONS.map(n => [n.type, n])
);

export function getNodeDefinition(type: string): NodeDefinition | undefined {
  return NODE_MAP[type];
}

export function getNodesByCategory(category: NodeCategory): NodeDefinition[] {
  return NODE_DEFINITIONS.filter(n => n.category === category);
}

export function searchNodes(query: string): NodeDefinition[] {
  const q = query.toLowerCase().trim();
  if (!q) return NODE_DEFINITIONS;
  return NODE_DEFINITIONS.filter(n =>
    n.name.toLowerCase().includes(q) ||
    n.description.toLowerCase().includes(q) ||
    n.type.toLowerCase().includes(q) ||
    (n.tags ?? []).some(t => t.toLowerCase().includes(q))
  );
}

