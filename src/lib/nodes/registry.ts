export type NodeCategory =
  | 'triggers'
  | 'ai'
  | 'actions'
  | 'logic'
  | 'code'
  | 'databases'
  | 'communication'
  | 'productivity'
  | 'devops'
  | 'transform'
  | 'utilities';

export type FieldType = 'string' | 'text' | 'number' | 'boolean' | 'select' | 'json' | 'code' | 'credentials';

export interface NodeField {
  key: string;
  label: string;
  type: FieldType;
  required?: boolean;
  placeholder?: string;
  default?: unknown;
  options?: { label: string; value: string }[];
  language?: 'javascript' | 'python' | 'json';
  help?: string;
}

export interface NodeDefinition {
  type: string;
  name: string;
  description: string;
  category: NodeCategory;
  icon: LucideIcon;
  color: string;
  inputs: number;
  outputs: number;
  fields: NodeField[];
  run?: (inputs: Record<string, unknown>, config: Record<string, unknown>) => Promise<Record<string, unknown>>;
  popular?: boolean;
  tags?: string[];
}

